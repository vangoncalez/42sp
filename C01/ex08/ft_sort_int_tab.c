void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	temp;
	int	swap;	

	swap = 1;
	while (swap > 0)
	{
		i = 0;
		swap = 0;
		while (i < size - 1)
		{
			if (tab[i + 1] < tab[i])
			{
				temp = tab[i];
				tab[i] = tab[i + 1];
				tab[i + 1] = temp;
				swap = 1;
			}
			i++;
		}
	}
}
